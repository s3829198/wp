            <h3>This year is the centenary of the birth of the ANZAC legend. As such, many people, particularly young people, are looking for ways to connect with people of that period and inparticular, those who created the ANZAC legend.</h3>
            <h3>This site presents the letters of Douglas Raymond Baker, who came from Gympie, Queensland, Australia. He enlisted in August 1914 and during the years that followed, he wrote letters and post cards to his family at home. In them, he describes much of the goings-on of the life of an ANZAC, his feeling and opinions, and experiences while visiting his relatives in England during leave.<br><img src="../../media/D.R.%20Baker%20F.Co%20one%20of%20the%20soldiers%20photographed%20in%20The%20Queenslander%20Pictorial%20supplement%20to%20The%20Queenslander%201914..jpg" class="Baker" alt="D.R. Baker" width="130" height="200"><br>They start from the beginning of basic training in Brisbane in August 1914 and end in May 1918.<br><br>They are offered here so that others may get an understanding of life as an ANZAC and get a sense of what life on the battlefield was like.<br><br>From the menu on the left, read the Introduction to set the scene. Then, to start reading the letters, click on Letter and Post Cards in the menu on the left. All the letters are searchable using the  search bar at the top right of this page.</h3>
